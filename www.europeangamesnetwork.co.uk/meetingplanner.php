<h1 class="meetingPlanner">The Meeting Planner</h1>

      <p style="float:right; width:230px; border:1px solid black; background:red; padding:5px"><b>Please 
        note -The Meeting Planner is now CLOSED. If you have any meetings or sessions 
        scheduled via the Meeting Planner, you will receive a copy of your Personal 
        Agenda together with your badge upon registering onsite.</b></p>	

      <p>EGN is focused around creating business opportunities for the games industry. The emphasis is on pre-scheduled meetings and structured networking opportunities, supported by a comprehensive conference and seminar programme. </p>

<p>The EGN Meeting Planner offers Attendees, delegates and exhibitors, an ideal way to plan their business meetings and conference/seminar attendance and so maximise their time. </p>

<p><strong>How it works?</strong></p>

<p>Every qualifying invitee is provided with their own password protected web facility, from which they can set-up pre-arranged meetings. Upon registering, Attendees are invited to submit a brief personal profile, outlining their primary business activities and meeting interests. </p>

<p>Attendee profiles are posted to a password protected section of the EGN web site, without any contact information being displayed. </p>

      <p>From the 2 August, Attendees will be invited to visit the EGN Meeting 
        Planner website to identify other participants with matching interests, 
        submit meeting requests, and register for the conference/seminar sessions 
        of most interest. Search facilities are available to help Attendees identify 
        industry colleagues whom they wish to meet with. Reasoned meeting requests 
        can then be initiated and responded to. </p>

<p>When two parties agree to meet, a brief twenty-minute appointment will be scheduled to take place at a mutually convenient time during EGN.  </p>

<p>One week prior to the event, each Attendee will be able to view their pre-scheduled programme of appointments and pre-booked conference/seminar sessions, in the form of a Personal Agenda.  A printed version of the Personal Agenda is provided to each Attendee when arriving at the event. </p>

<p>Throughout the duration of EGN, a mobile phone text messaging system will be operational, for communicating any messages to Attendees relating to their pre-arranged Personal Agenda programme. </p>

      <p>If you have any questions on how to make best use of the EGN Meeting 
        Planner, please send an <a href=mailto:ng@entevents.co.uk?subject=EGN%20Meeting%20Planner%20Queries Meeting Planner queries>email</a> 
        to Nathalie Gsell, Structured Networking Manager, EGN</p>


