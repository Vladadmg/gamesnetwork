			<h1>About Us</h1>
		
      <h2>About ELSPA</h2>
      <p><strong>ELSPA</strong> (the Entertainment & Leisure Software Publishers 
        Association) was founded in 1989 to establish a specific and collective 
        identity for the computer and video games industry. Membership includes 
        almost all companies concerned with the publishing and distribution of 
        interactive leisure software in the UK.</p>
<p><strong>ELSPA</strong>'s activities include: Official Chart and Industry Reports, Anti-Piracy UK and EU, PR and Communication, Events.  More information on all these activities can be found at <a href="http://www.elspa.com">http://www.elspa.com</a>.</p>

<h2>About TIGA</h2>
<p><strong>TIGA</strong> (The Independent Game developers Association) is the trade association which represents the business and commercial interests of the UK's Independent game developers - i.e. those that are not controlled by publishers and whose principal business is the development of games. Launched in March 2001, by DTI Minister <strong>TIGA</strong> has more than 90 member companies that collectively represent expertise from right across the industry. </p>
<p><strong>TIGA </strong>works to raise the bargaining power of the UK developer community promoting communication between developers and publishers and those that provide services to them: <strong>TIGA</strong> aims to benefit its members' businesses and creative interests by encouraging a better climate for investment in games, best business practice, dialogue and favourable terms with publishers and by lobbying for government support to the industry.<br />
<a href=http://www.tiga.org>www.tiga.org</a>
</p>
      <h2>About London EventCo</h2>
      <p><strong>London EventCo</strong> is the in-house exhibition organising 
        company at ExCeL, created to launch major consumer and trade shows at 
        London's prestigious new venue in the heart of the Docklands.</p>
      <p>An entrepreneurial company, committed to launch and growth, it is run 
        by a highly creative and innovative team with extensive experience in 
        the consumer and trade exhibitions industry.</p>
<h2>About ExCeL</h2>
<p><strong>ExCeL</strong> is London's largest events, exhibitions and conference venue, located on a 100-acre site alongside Royal Victoria Dock in London's Docklands, between Canary Wharf and London City airport. </p>
      <p><strong>ExCeL</strong> already hosts some of the UK and Europe's biggest 
        events, including World Travel Market, The London International Boat Show, 
        Max Power London, Toy Fair, The London International Wine & Spirits Fair, 
        IFE-International Food & Drink Exhibition and Salon International.</p>

      <p>For more information on <strong>ExCeL</strong>, what's on and how to get to 
        <strong>ExCeL</strong>, please visit <a href="http://www.excel-london.co.uk">http://www.excel-london.co.uk</a>.</p>


