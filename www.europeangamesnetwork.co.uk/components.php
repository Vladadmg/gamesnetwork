			<h1 class="components">Components</h1>
	
<h2 id="marketPlace">The Market Place</h2>
<p>In the heart of EGN, the Market Place is a conventional trade floor area where all visitors and delegates may circulate freely. Companies will exhibit via high specification standardised stands with no free builds and a maximum stand size of 120m2 ensuring a level playing field for small and large exhibitors.</p>

<h2 id="waterfrontRooms">The Waterfront Rooms </h2>
<p>These stunningly-situated meeting rooms immediately accessible from the main EGN Exhibition Hall will be taken by exhibitors wishing to run closed-shop demonstrations, invitation-only briefings, product launches and private VIP hospitality.</p>

<h2 id="hubClub">The Hub Club </h2>
<p>The Hub Club is a restricted-access business area adjacent to The Market Place where our VIP and Hub Club Delegates can host their own meetings and network with others at both formal meeting tables and informally at the Hub Club Bar.</p>

<p>VIP delegates will enjoy the facility on a free of charge basis. Hub Club Delegates will pay a fee for the use of the Hub Club over the three days of EGN and will additionally have access to the Meeting Planner and the full Conference and Seminar Programme</p>

<h2 id="tiga">The TIGA International Content Market</h2> 
<p>Developed as a TIGA event within EGN, The TIGA International Content Market will create a highly exciting programme of activity within a dedicated business forum for developers. It will be hosted on the main floor of EGN, covering approximately 600m2 and will consist of a mix of open plan meeting areas and closed presentation rooms for private meetings and demonstrations.</p>

<h2 id="netBar">The Net Bar</h2>
<p>An unrestricted access bar in the heart of EGN offering an informal and open environment for all levels of delegate and exhibitors to freely meet, relax, drink and network</p>

<h2 id="meetingPlanner">The Meeting Planner</h2>
      <p>VIP and Hub Club delegates will have the opportunity to plan their day 
        at EGN using the <a href="meetingplanner.php">Meeting Planner</a> - a 
        sophisticated internet based schedule facilitator which will allow pre-scheduling 
        of meetings with exhibitors and pre-booking opportunities for high demand 
        sessions in the Conference and Seminar Programme.</p>

