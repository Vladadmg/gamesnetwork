<h2>European Games Network is the complete exhibition for everyone in the business of games.
WHY VISIT?</h2>
<ul>
	<li>Backed by ELSPA, EGN has been created by the industry, for the industry</li>
	<li>Using the Meeting Planner, your visit to EGN will be highly productive and time efficient as you will be able to pre-schedule meetings with key clients and business associates.</li>
	<li>Gain inspiration from the leading names in the games industry, exhibiting their latest products and ideas</li> 
	<li>Companies exhibiting from across the globe </li>
	<li>The perfect opportunity to network for all levels of business</li>
	<li>Visit the Market Place, and the TIGA International Content Market to find out about the latest industry developments </li>
	<li>Visit the Hub Club and network with the top industry players</li>
	<li>Free Conference and Seminar programme - industry experts share their experiences</li>
	</ul>
	
	<p>EGN will run alongside Game Stars Live at ExCeL from 1-3 September 2004.</p>
	<p>Game Stars Live, a spin off from ITV's highly successful Game Stars TV programme, will be a 5-day interactive computer games festival for the consumer representing all types of gaming from console, pc and networked, to mobile, handheld and online. </p>
<p>Game Stars Live has and is continuing to receive massive media attention from both the games industry and the general press.  
Visit <a href="http://www.gamestarslive.co.uk">www.gamestarslive.co.uk</a> for more details on this groundbreaking new show.</p>

