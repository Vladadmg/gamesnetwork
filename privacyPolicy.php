			<h1 class="privacyPolicy">Privacy Policy</h1>
			
<p>This policy outlines the type of information we gather from this Web Site and the manner in which we use this information. Please read the following information carefully before submitting information or using this site.</p>

<p>The European Games Network (EGN) website is designed to provide a convenient means for you to obtain information about EGN and to register for this event. </p>

<p>There are two occasions when EGN may ask you provide personal Information including name, title, company, address, phone and fax number, email address, as well as some demographic information such as Job Function, Product Interests, Primary Business and Purchase Involvement. </p>

<p>When you register for EGN, you are required to provide Personal Information as well as demographic information; this information is required to process your registration. Your registration will not be processed unless the required fields have been completed. </p>

<p>At other areas of the Web site, you may provide your Personal Information on a voluntary basis in order to receive email updates about the event or join the mailing list. </p>

<p>In addition, the EGN web server collects the domain names of the visitors that come to our site. This information is collected to measure the number of visits, the amount of time spent on the site, how many pages were viewed, etc. and this information may be distributed to EGN exhibitors for advertising purposes. </p>

<p>The Personal Information that we collect is used to process your registration or may be used by EGN and ELSPA for marketing and promotional purposes to keep you informed with the latest news about the event. This information may be shared with carefully selected third parties only through a registered mailing house. </p>

<p>If you have provided EGN with your Personal Information, you will receive promotional pieces about the event in the mail, by fax or via email. If, at any time, you decide you do not wish to receive further information about EGN, you may remove yourself from the email or mailing list by returning the promotional piece with the word "remove". EGN continually updates its mailing and email lists and your name will be promptly removed.</p>

<p>When you receive promotional information from EGN, you may change any incorrect Personal Information and notify EGN by returning the piece with the corrected information. This information will be corrected immediately. </p>

<p>If you have concerns with how your information is being used, you may, at any time, request in writing via the EGN webmaster to review the information which has been collected. </p>



